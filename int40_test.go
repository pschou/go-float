package float_test

import (
	"fmt"

	"github.com/pschou/go-float"
)

func ExampleUInt40() {
	b := []byte{0, 0}
	for i := 1; i < 9900000; i = i << 1 {
		float.PutUInt40(b, uint(i))
		fmt.Println(i, b, float.UInt40(b))
	}
	fmt.Println("Maximum 15bit", float.UInt40([]byte{0x7f, 0xff}))

	// Output:
	// 1 [0 1] 1
	// 2 [0 2] 2
	// 4 [0 4] 4
	// 8 [0 8] 8
	// 16 [0 16] 16
	// 32 [0 32] 32
	// 64 [0 64] 64
	// 128 [0 128] 128
	// 256 [1 0] 256
	// 512 [2 0] 512
	// 1024 [4 0] 1024
	// 2048 [8 0] 2048
	// 4096 [16 0] 4096
	// 8192 [32 0] 8192
	// 16384 [41 142] 16380
	// 32768 [47 245] 32770
	// 65536 [60 194] 65540
	// 131072 [75 111] 131100
	// 262144 [80 141] 262100
	// 524288 [90 203] 524300
	// 1048576 [109 145] 1049000
	// 2097152 [113 169] 2097000
	// 4194304 [121 218] 4194000
	// 8388608 [138 61] 8389000
	// Maximum 15bit 5767000
}
