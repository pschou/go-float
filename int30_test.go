package float_test

import (
	"fmt"

	"github.com/pschou/go-float"
)

func ExampleUInt30() {
	b := []byte{0, 0}
	for i := 1; i < 9900000; i = i << 1 {
		float.PutUInt30(b, uint(i))
		fmt.Println(i, b, float.UInt30(b))
	}
	fmt.Println("Maximum 12bit", float.UInt30([]byte{0x0f, 0xff}))

	// Output:
	// 1 [0 1] 1
	// 2 [0 2] 2
	// 4 [0 4] 4
	// 8 [0 8] 8
	// 16 [0 16] 16
	// 32 [0 32] 32
	// 64 [0 64] 64
	// 128 [0 128] 128
	// 256 [1 0] 256
	// 512 [2 0] 512
	// 1024 [3 234] 1020
	// 2048 [4 81] 2050
	// 4096 [5 30] 4100
	// 8192 [6 183] 8190
	// 16384 [7 172] 16400
	// 32768 [8 80] 32800
	// 65536 [9 151] 65500
	// 131072 [11 15] 131000
	// 262144 [11 146] 262000
	// 524288 [12 152] 524000
	// 1048576 [14 121] 1050000
	// 2097152 [14 226] 2100000
	// 4194304 [15 179] 4190000
	// 8388608 [17 87] 8390000
	// Maximum 12bit 4950000
}
