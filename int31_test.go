package float_test

import (
	"fmt"

	"github.com/pschou/go-float"
)

func ExampleUInt31() {
	b := []byte{0, 0}
	for i := 1; i < 9900000; i = i << 1 {
		float.PutUInt31(b, uint(i))
		fmt.Println(i, b, float.UInt31(b))
	}
	fmt.Println("Maximum 13bit", float.UInt31([]byte{0x1f, 0xff}))

	// Output:
	// 1 [0 1] 1
	// 2 [0 2] 2
	// 4 [0 4] 4
	// 8 [0 8] 8
	// 16 [0 16] 16
	// 32 [0 32] 32
	// 64 [0 64] 64
	// 128 [0 128] 128
	// 256 [1 0] 256
	// 512 [2 0] 512
	// 1024 [3 237] 1025
	// 2048 [4 186] 2050
	// 4096 [6 83] 4095
	// 8192 [9 134] 8190
	// 16384 [11 112] 16400
	// 32768 [12 183] 32750
	// 65536 [15 71] 65550
	// 131072 [18 54] 131000
	// 262144 [19 60] 262000
	// 524288 [21 73] 524500
	// 1048576 [25 10] 1050000
	// 2097152 [25 219] 2095000
	// 4194304 [27 127] 4195000
	// 8388608 [30 198] 8390000
	// Maximum 13bit 9955000
}
