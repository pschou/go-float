package float_test

import (
	"fmt"

	"github.com/pschou/go-float"
)

func ExampleUInt41() {
	b := []byte{0, 0}
	for i := 1; i < 9900000; i = i << 1 {
		float.PutUInt41(b, uint(i))
		fmt.Println(i, b, float.UInt41(b))
	}
	fmt.Println("Maximum 16bit", float.UInt41([]byte{0xff, 0xff}))

	// Output:
	// 1 [0 1] 1
	// 2 [0 2] 2
	// 4 [0 4] 4
	// 8 [0 8] 8
	// 16 [0 16] 16
	// 32 [0 32] 32
	// 64 [0 64] 64
	// 128 [0 128] 128
	// 256 [1 0] 256
	// 512 [2 0] 512
	// 1024 [4 0] 1024
	// 2048 [8 0] 2048
	// 4096 [16 0] 4096
	// 8192 [32 0] 8192
	// 16384 [44 13] 16385
	// 32768 [56 218] 32770
	// 65536 [82 115] 65535
	// 131072 [111 205] 131050
	// 262144 [122 11] 262150
	// 524288 [142 134] 524300
	// 1048576 [180 17] 1048500
	// 2097152 [188 66] 2097000
	// 4194304 [204 165] 4194500
	// 8388608 [237 105] 8388500
	// Maximum 16bit 17675000
}
